import React, { useState } from 'react';
import { Modal, View, Text, TextInput, TouchableOpacity, StyleSheet, Dimensions, TouchableWithoutFeedback } from 'react-native';
import Ionicons from 'react-native-vector-icons/Ionicons';
import DatePicker from 'react-native-date-picker'; // Assuming you use this for date selection
import moment from 'moment'; // For date formatting

const { width, height } = Dimensions.get('window');

const AddClientModal = ({ isVisible, onClose, onSave }) => {
    // Client ID state is removed, as it will be generated by the parent
    const [clientName, setClientName] = useState('');
    const [phoneNumber, setPhoneNumber] = useState('');
    const [comingDate, setComingDate] = useState(new Date()); // Initialize with current date
    const [openDatePicker, setOpenDatePicker] = useState(false); // State for date picker visibility

    const [customAlertVisible, setCustomAlertVisible] = useState(false);
    const [customAlertMessage, setCustomAlertMessage] = useState('');

    const showCustomAlert = (message) => {
        setCustomAlertMessage(message);
        setCustomAlertVisible(true);
    };

    const hideCustomAlert = () => {
        setCustomAlertVisible(false);
        setCustomAlertMessage('');
    };

    const resetForm = () => {
        setClientName('');
        setPhoneNumber('');
        setComingDate(new Date());
    };

    const handleSave = () => {
        const trimmedClientName = clientName.trim();
        const trimmedPhoneNumber = phoneNumber.trim();

        if (!trimmedClientName || !trimmedPhoneNumber) {
            showCustomAlert('Missing Information: Please fill in Client Name and Phone Number.');
            return;
        }

        const newClientData = {
            name: trimmedClientName,
            phoneNumber: trimmedPhoneNumber,
            comingDate: moment(comingDate).format('MMMM DD, YYYY'), // Format date before sending
        };
        
        onSave(newClientData); // Send data (without ID) to parent
        resetForm();
        onClose();
    };

    const handleClose = () => {
        resetForm();
        onClose();
    };

    return (
        <Modal
            animationType="fade"
            transparent={true}
            visible={isVisible}
            onRequestClose={handleClose}
        >
            <TouchableWithoutFeedback onPress={handleClose}>
                <View style={styles.centeredView}>
                    <TouchableWithoutFeedback>
                        <View style={styles.modalView}>
                            <View style={styles.modalHeader}>
                                <Text style={styles.modalTitle}>Add Direct New Client</Text>
                                <TouchableOpacity onPress={handleClose} style={styles.crossButton}>
                                    <Ionicons name="close-circle-outline" size={width * 0.03} color="#A9A9A9" />
                                </TouchableOpacity>
                            </View>

                            {/* Client ID input removed */}
                            <TextInput
                                style={styles.input}
                                placeholder="Client Name"
                                placeholderTextColor="#A9A9A9"
                                value={clientName}
                                onChangeText={setClientName}
                            />
                            <TextInput
                                style={styles.input}
                                placeholder="Phone Number"
                                placeholderTextColor="#A9A9A9"
                                keyboardType="phone-pad"
                                value={phoneNumber}
                                onChangeText={setPhoneNumber}
                            />
                            
                            {/* Coming Date Picker */}
                            <TouchableOpacity
                                style={styles.datePickerInput}
                                onPress={() => setOpenDatePicker(true)}
                            >
                                <Text style={styles.datePickerText}>
                                    {moment(comingDate).format('MMMM DD, YYYY')}
                                </Text>
                                <Ionicons name="calendar-outline" size={width * 0.018} color="#A9A9A9" />
                            </TouchableOpacity>

                            <DatePicker
                                modal
                                mode="date"
                                open={openDatePicker}
                                date={comingDate}
                                onConfirm={(date) => {
                                    setOpenDatePicker(false);
                                    setComingDate(date);
                                }}
                                onCancel={() => setOpenDatePicker(false)}
                            />

                            <View style={styles.buttons}>
                                <TouchableOpacity style={styles.closeButton} onPress={handleClose}>
                                    <Text style={styles.closeButtonText}>Close</Text>
                                </TouchableOpacity>
                                <TouchableOpacity style={styles.saveButton} onPress={handleSave}>
                                    <Text style={styles.saveButtonText}>Save</Text>
                                </TouchableOpacity>
                            </View>
                        </View>
                    </TouchableWithoutFeedback>
                </View>
            </TouchableWithoutFeedback>

            {/* Custom Alert Modal */}
            <Modal
                animationType="fade"
                transparent={true}
                visible={customAlertVisible}
                onRequestClose={hideCustomAlert}
            >
                <View style={styles.customAlertCenteredView}>
                    <View style={styles.customAlertModalView}>
                        <Text style={styles.customAlertModalText}>{customAlertMessage}</Text>
                        <TouchableOpacity
                            style={styles.customAlertCloseButton}
                            onPress={hideCustomAlert}
                        >
                            <Text style={styles.customAlertCloseButtonText}>OK</Text>
                        </TouchableOpacity>
                    </View>
                </View>
            </Modal>
        </Modal>
    );
};

